#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>

/**
 * op_add - Write a function that adds two integers.
 * @a: an Integer.
 * @b: another integer.
 * Return: a + b.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Write a function that substracts two integers.
 * @a: an Integer.
 * @b: another integer.
 * Return: a - b.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Write a function that multiplies two integers.
 * @a: an Integer.
 * @b: another integer.
 * Return: a * b.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Write a function that divides two integers.
 * @a: an Integer.
 * @b: another integer.
 * Return: a / b.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Write a function that mods two integers.
 * @a: an Integer.
 * @b: another integer.
 * Return: a % b.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
